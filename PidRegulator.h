#pragma once
#include "IRegulator.h"
#include <cmath>

/**
 * @brief ПИД-регулятор (Proportional-Integral-Derivative) для плавного управления параметрами
 *
 * Принцип работы:
 * ПИД-регулятор вычисляет управляющий сигнал (0-100%) на основе разницы между
 * текущим и целевым значением (ошибки). Сигнал состоит из трёх компонентов:
 *
 * Пропорциональный (P): реагирует на текущую ошибку. Чем больше ошибка, тем сильнее воздействие.
 * Интегральный (I): накапливает ошибку во времени, устраняет установившуюся ошибку.
 * Дифференциальный (D): реагирует на скорость изменения ошибки, сглаживает колебания.
 *
 * Формула: output = P * error + I * ∫error dt + D * d(error)/dt
 */
class PidRegulator : public IRegulator {
public:
    /**
     * @brief Конструктор ПИД-регулятора
     *
     * @param kp Пропорциональный коэффициент (P). Лучше диапазон: 0.5-5.0
     *      Определяет, насколько сильно регулятор реагирует на текущую ошибку.
     *      Слишком высокое значение вызывает колебания, слишком низкое - медленную реакцию.
     *
     * @param ki Интегральный коэффициент (I). Лучше диапазон: 0.01-0.5
     *      Определяет скорость устранения установившейся ошибки.
     *
     * @param kd Дифференциальный коэффициент (D). Лучше диапазон: 0.1-2.0
     *      Определяет демпфирующий эффект, предсказывая будущую ошибку.
     *      Помогает предотвратить колебания.
     *
     * @param dt Временной шаг в секундах между вызовами calculate()
     *      Должен соответствовать частоте вызова регулятора
     */
    PidRegulator(double kp = 2.0, double ki = 0.1, double kd = 0.5, double dt = 1.0);

    /**
     * @brief Вычисление управляющего сигнала на основе текущего и целевого значения
     *
     * Алгоритм:
     * 1. Вычисление ошибки = целевое - текущее
     * 2. Пропорциональная составляющая: P = Kp * error
     * 3. Интегральная составляющая: I += error * dt, затем I = Ki * I
     * 4. Дифференциальная составляющая: D = Kd * (error - prevError) / dt
     * 5. Суммирование: output = P + I + D
     * 6. Ограничение выхода в диапазоне [0, 100]
     *
     * @param current Текущее значение параметра (температура, влажность и т.д.)
     * @param target Желаемое (целевое) значение параметра
     * @return Управляющий сигнал в процентах (0-100), где 0 = выключено, 100 = полная мощность
     */
    double calculate(double current, double target) override;

    /**
     * @brief Сброс внутреннего состояния регулятора
     *
     * Обнуляет интегральную составляющую и предыдущую ошибку.
     * Используется при смене целевого параметра или перезапуске системы.
     */
    void reset() override;

    /**
     * @brief Получить название регулятора
     * @return Строка "PidRegulator"
     */
    std::string getName() const override;

    /**
     * @brief Динамическая перенастройка коэффициентов регулятора
     *
     * Позволяет изменять поведение регулятора во время работы.
     * После изменения коэффициентов сбрасывается внутреннее состояние.
     *
     * @param kp Пропорциональный коэффициент
     * @param ki Интегральный коэффициент
     * @param kd Дифференциальный коэффициент
     */
    void setGains(double kp, double ki, double kd);

private:
    // Коэффициенты ПИД-регулятора
    double m_kp;    ///< Пропорциональный коэффициент (P) - чувствительность к текущей ошибке
    double m_ki;    ///< Интегральный коэффициент (I) - устранение установившейся ошибки
    double m_kd;    ///< Дифференциальный коэффициент (D) - устранение колебаний

    // Внутреннее состояние регулятора
    double m_integral;      ///< Накопленная сумма ошибок (интегральная составляющая)
    double m_prevError;     ///< Предыдущее значение ошибки (для вычисления производной)
    double m_dt;            ///< Временной шаг между вызовами calculate() в секундах
};