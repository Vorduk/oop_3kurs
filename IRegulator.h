#pragma once
#include <string>

/**
 * @brief Интерфейс для алгоритмов регулирования
 *
 * Определяет интерфейс для всех алгоритмов, вычисляющих управляющий сигнал
 * на основе отклонения текущего значения от целевого.
 */
class IRegulator {
public:
    virtual ~IRegulator() = default;

    /**
     * @brief Рассчитать управляющий сигнал
     *
     * Вычисляет значение управляющего воздействия на основе текущего
     * и целевого значения параметра.
     *
     * @param current Текущее значение параметра (например, 22.5 °C)
     * @param target Целевое значение параметра (например, 23.0 °C)
     * @return Управляющий сигнал в процентах (0-100), где:
     *   0 - устройство выключено,
     *   100 - максимальная мощность/интенсивность
     */
    virtual double calculate(double current, double target) = 0;

    /**
     * @brief Сбросить внутреннее состояние регулятора
     *
     * Необходим для регуляторов, накапливающих историю (например, PID).
     * Вызывается при смене целевых параметров или перезапуске системы.
     */
    virtual void reset() {}

    /**
     * @brief Получить название регулятора
     *
     * Для логирования и отладки.
     *
     * @return Строковое имя регулятора (например, "PidRegulator", "OnOffRegulator")
     */
    virtual std::string getName() const = 0;
};
