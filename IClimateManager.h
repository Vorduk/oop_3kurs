#pragma once
#include <map>
#include <string>

/**
 * @brief Интерфейс менеджера климата
 *
 * Определяет набор методов, которые должны реализовывать все классы,
 * отвечающие за управление микроклиматом в теплице.
 * Реализации этого интерфейса отвечают за расчет управляющих воздействий
 * на основе текущих показаний датчиков и целевых параметров.
 */
class IClimateManager {
public:
    virtual ~IClimateManager() = default;

    /**
     * @brief Установить целевые параметры микроклимата
     *
     * Задает желаемые значения параметров, к которым система должна стремиться.
     * Ключи — имена параметров ("temperature", "air_humidity", "soil_moisture").
     *
     * @param targets Ассоциативный массив параметр -> целевое значение
     *
     * @note Целевые параметры сохраняются внутри менеджера и используются
     *       при каждом вызове calculateCommands().
     */
    virtual void setTargetParameters(const std::map<std::string, double>& targets) = 0;

    /**
     * @brief Рассчитать управляющие команды для устройств
     *
     * На основе текущих показаний датчиков и ранее установленных целевых
     * параметров вычисляет, какие устройства и с какой мощностью должны работать.
     *
     * @param currentReadings Текущие показания всех датчиков
     *                         (параметр -> текущее значение)
     *
     * @return std::map<std::string, int> команда -> мощность
     *         Ключ: тип устройства ("heater", "conditioner", "air_humidifier" и т.д.)
     *         Значение: уровень мощности в процентах (0-100)
     *
     * @note Положительное значение для устройства означает "включить с указанной мощностью"
     *       Значение 0 означает "выключить устройство"
     */
    virtual std::map<std::string, int> calculateCommands(
        const std::map<std::string, double>& currentReadings) = 0;
};

